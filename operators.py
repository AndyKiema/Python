#Arithmetic operators
a=100
b=200
print(a+b) #300
sum=a+b
print(sum)#300
print(a*b)#20000
print(b-a)#100
div=b/a
print(div)#2.0
print(type(div)) #<class 'float'>
print(a//b)#returns co-efficient a/b=0.5. So it returns 0;
print(b%a)# returns 0, which is the remainder
print(a**3)#a to the power of 3 wich is 1,000,000

#Relational operators
#Always returns a boolean value
print(a>b)#False
print(b>a)#True
print(a!=b)#True
print(a==b)#False
print(a<=b)#True
print(a>=b)#False

#Logical operators
x=True
y=False
print(x and y) #False
print(x or y) #True
print(not x) #False
print(not y) #True
